COLORS = {
    "RED": "\u001b[31m",
    "GREEN": "\u001b[32m",
    "CYAN": "\u001b[36m",
    "BLUE": "\u001b[34m",
    "RESET": "\u001b[0m",
    "MAGENTA": "\u001b[35m",
    "YELLOW": "\u001b[33m",
    "WHITE": "\u001b[37m",
    "LIGHTBLUE": "\u001b[34;1m",
    "BRIGHTRED": "\u001b[31;1m"
}

alphabet = string.ascii_uppercase
portals = {}
portal_pos = {}
steps = 1


def printMaze(maze, rows, cols):
    
    for i in range(0, rows):
        for j in range(0, cols):
            
            if maze[i][j] == 'x':
                print(f"{COLORS['LIGHTBLUE']}{maze[i][j]}{COLORS['RESET']} ", end="")
            elif type(maze[i][j]) == int:
                print(f"{COLORS['CYAN']}{maze[i][j]}{COLORS['RESET']} ", end="")
            elif maze[i][j] == '>' or maze[i][j] == '<' or maze[i][j] == '^' or maze[i][j] == 'v':
                print(f"{COLORS['GREEN']}{maze[i][j]}{COLORS['RESET']} ", end="")
            elif maze[i][j] == '#':
                print(f"{COLORS['BRIGHTRED']}{maze[i][j]}{COLORS['RESET']} ", end="")
            else:
                print(f"{maze[i][j]} ", end="")
        print('\n')
    time.sleep(2.4)

    

def solveMaze(maze, i, j, rows, cols):

    global steps

    printMaze(maze, rows, cols)
    
    #time.sleep(2.4)

    # EXIT CONDITION 
    if maze[i + 1][j] == 'e':
        print(f"Total number of steps: {steps}")

        maze[i][j] = 'v'
        exit(1)
    if maze[i][j + 1] == 'e':
        print(f"Total number of steps: {steps}")

        maze[i][j] = '>'
        exit(1)
    if maze[i - 1][j] == 'e':
        print(f"Total number of steps: {steps}")

        maze[i][j] = '^'
        exit(1)
    if maze[i][j - 1] == 'e':
        print(f"Total number of steps: {steps}")
        maze[i][j] = '<'
        exit(1)
    


    # GO INTO AN EXIT/ENTRANCE PORTAL

    if type(maze[i + 1][j]) == int:         # portal bottom
        

        p = maze[i + 1][j]

        if maze[i][j] != '^':     # do not go into the portal u've just came out of

            if portal_pos[p][0] == i + 1 and portal_pos[p][1] == j:   # if its entrance 
                #warp to exit
                steps += 1
                solveMaze(maze, portal_pos[p][2], portal_pos[p][3], rows, cols)

            elif portal_pos[p][2] == i + 1 and portal_pos[p][3] == j:       # from exit portal you entered exit portal 
                #warp to entrance
                steps += 1
                solveMaze(maze, portal_pos[p][0], portal_pos[p][1], rows, cols)

        else:
            pass
        
    elif type(maze[i][j + 1]) == int:       # portal right


        p = maze[i][j + 1]

        if maze[i][j] != '<':     # do not go into the portal u've just came out of

            if portal_pos[p][0] == i and portal_pos[p][1] == j + 1:
                steps += 1
                solveMaze(maze, portal_pos[p][2], portal_pos[p][3], rows, cols)

            elif portal_pos[p][2] == i and portal_pos[p][3] == j + 1:
                steps += 1
                solveMaze(maze, portal_pos[p][0], portal_pos[p][1], rows, cols)

        else:
            pass


    elif type(maze[i - 1][j]) == int:       # portal up

        p = maze[i - 1][j]

        if maze[i][j] != 'v':     # do not go into the portal u've just came out of
            if portal_pos[p][0] == i - 1 and portal_pos[p][1] == j:
                steps += 1
                solveMaze(maze, portal_pos[p][2], portal_pos[p][3], rows, cols)
            elif portal_pos[p][2] == i - 1 and portal_pos[p][3] == j: 
                steps += 1
                solveMaze(maze, portal_pos[p][0], portal_pos[p][1], rows, cols)

        else:
            pass

    elif type(maze[i][j - 1]) == int:       # portal left
        
        p = maze[i][j - 1]

        # print(f"my direction: {maze[i][j]}")
        # exit(1)
        if maze[i][j] != '>':     # do not go into the portal u've just came out of
        
            if portal_pos[p][0] == i and portal_pos[p][1] == j - 1:
                steps += 1
                solveMaze(maze, portal_pos[p][2], portal_pos[p][3], rows, cols)

            elif portal_pos[p][2] == i and portal_pos[p][3] == j - 1:
                steps += 1
                solveMaze(maze, portal_pos[p][0], portal_pos[p][1], rows, cols)

        else:                                # im coming out of an exit portal
            pass



     # STEP IN ONE OF 4 DIRETIONS IF ITS A CLEAR PATH, RECURSE 
    if  maze[i + 1][j] == '.':
        maze[i + 1][j] = 'v'
        steps += 1
        solveMaze(maze, i + 1, j, rows, cols)           # go down


    if maze[i][j + 1] == '.':
        maze[i][j + 1] = '>'
        steps += 1
        solveMaze(maze, i, j + 1, rows, cols)           # go right



    if maze[i][j - 1] == '.':                           # go left
        maze[i][j - 1] = '<'
        steps += 1
        solveMaze(maze, i, j - 1, rows, cols)


    if  maze[i - 1][j] == '.':                          # go up
        maze[i - 1][j] = '^'
        steps += 1
        solveMaze(maze, i - 1, j, rows, cols)      
        


    maze[i][j] = 'x'
    steps -= 1



def parseMaze(input):

    input = input.split('\n')

    cols = len(input[0])
    rows = len(input)
    
    maze = []
    for i in range(0, rows):
        _r = [char for char in input[i]]
        maze.append(_r)



    portal = 1
    # outer top part
    for i in range(0, 2):
        for j in range(0, cols):
            if maze[i][j] != ' ':
                if maze[i][j] == 'A' and maze[i + 1][j] == 'A':   
                    maze[i + 2][j] = 's'
                    maze[i][j] = ' '
                    maze[i + 1][j] = ' '
                    portal_pos['s'] = (i + 2, j)
                elif maze[i][j] == 'Z' and maze[i + 1][j] == 'Z':
                    maze[i + 2][j] = 'e'
                    maze[i][j] = ' '
                    maze[i + 1][j] = ' '
                    portal_pos['e'] = (i + 2, j)
                else:       # portal found
                    portals[maze[i][j] + maze[i + 1][j]] = portal # memorize
                    portal_pos[portal] = [0, 0, i + 2, j]     # ((entrace_i, entrace_j), (exit_i, exit_j))
                    maze[i + 2][j] = portal
                    maze[i][j] = ' '
                    maze[i + 1][j] = ' '
                    portal += 1


    # outer left part
    for i in range(0, rows):
        for j in range(0, 2):
            if maze[i][j] != ' ':
                if maze[i][j] == 'A' and maze[i][j + 1] == 'A':   
                    maze[i][j + 2] = 's'
                    maze[i][j] = ' '
                    maze[i][j + 1] = ' '
                    portal_pos['s'] = (i, j + 2)
                elif maze[i][j] == 'Z' and maze[i][j + 1] == 'Z':
                    maze[i][j + 2] = 'e'
                    maze[i][j] = ' '
                    maze[i][j + 1] = ' '
                    portal_pos['e'] = (i, j + 2)
                else:       # portal found
                    portals[maze[i][j] + maze[i][j + 1]] = portal # memorize
                    portal_pos[portal] = [0, 0, i, j + 2]
                    maze[i][j + 2] = portal
                    maze[i][j] = ' '
                    maze[i][j + 1] = ' '
                    portal += 1

    # outer bottom part
    for i in range(rows - 1, rows - 2, -1):
        for j in range(0, cols):
            if maze[i][j] != ' ':
                if maze[i][j] == 'A' and maze[i - 1][j] == 'A':   
                    maze[i - 2][j] = 's'
                    maze[i][j] = ' '
                    maze[i - 1][j] = ' '
                    portal_pos['s'] = (i - 2, j)
                elif maze[i][j] == 'Z' and maze[i - 1][j] == 'Z':
                    maze[i - 2][j] = 'e'
                    maze[i][j] = ' '
                    maze[i - 1][j] = ' '
                    portal_pos['e'] = (i - 2, j)
                else:       # portal found
                    portals[maze[i - 1][j] + maze[i][j]] = portal # memorize
                    portal_pos[portal] = [0, 0, i - 2, j]
                    maze[i - 2][j] = portal
                    maze[i][j] = ' '
                    maze[i - 1][j] = ' '
                    portal += 1



        # outer right part
    for i in range(0, rows):
        for j in range(cols - 1, cols - 2, -1):
            if maze[i][j] != ' ':
                if maze[i][j] == 'A' and maze[i][j - 1] == 'A':   
                    maze[i][j - 2] = 's'
                    maze[i][j] = ' '
                    maze[i][j - 1] = ' '
                    portal_pos['s'] = (i, j - 2)
                elif maze[i][j] == 'Z' and maze[i][j - 1] == 'Z':
                    maze[i][j - 2] = 'e'
                    maze[i][j] = ' '
                    maze[i][j - 1] = ' '    
                    portal_pos['e'] = (i, j - 2)
                else:       # portal found
                    portals[maze[i][j - 1] + maze[i][j]] = portal # memorize
                    portal_pos[portal] = [0, 0, i, j - 2]
                    maze[i][j - 2] = portal
                    maze[i][j] = ' '
                    maze[i][j - 1] = ' '
                    portal += 1

