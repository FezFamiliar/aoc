portals = {}

def printMaze(maze, rows, cols):
    for i in range(0, rows):
        for j in range(0, cols):
            print(f"{maze[i][j]} ", end="")
        print()


def parseMaze(input):

    input = input.split('\n')

    cols = len(input[0])
    rows = len(input)
    
    print(f"cols = {cols}")
    print(f"rows = {rows}")
    maze = []
    for i in range(0, rows):
        _r = [char for char in input[i]]
        maze.append(_r)



    portal = 1
    # outer top part
    for i in range(0, 2):
        for j in range(0, cols):
            if maze[i][j] != ' ':
                if maze[i][j] == 'A' and maze[i + 1][j] == 'A':   
                    maze[i + 2][j] = 's'
                    maze[i][j] = ' '
                    maze[i + 1][j] = ' '
                elif maze[i][j] == 'Z' and maze[i + 1][j] == 'Z':
                    maze[i + 2][j] = 'e'
                    maze[i][j] = ' '
                    maze[i + 1][j] = ' '
                else:       # portal found
                    portals[maze[i][j] + maze[i + 1][j]] = portal # memorize
                    maze[i + 2][j] = str(portal)
                    maze[i][j] = ' '
                    maze[i + 1][j] = ' '
                    portal += 1


    # outer left part
    for i in range(0, rows):
        for j in range(0, 2):
            if maze[i][j] != ' ':
                if maze[i][j] == 'A' and maze[i][j + 1] == 'A':   
                    maze[i][j + 2] = 's'
                    maze[i][j] = ' '
                    maze[i][j + 1] = ' '
                elif maze[i][j] == 'Z' and maze[i][j + 1] == 'Z':
                    maze[i][j + 2] = 'e'
                    maze[i][j] = ' '
                    maze[i][j + 1] = ' '
                else:       # portal found
                    portals[maze[i][j] + maze[i][j + 1]] = portal # memorize
                    maze[i][j + 2] = str(portal)
                    maze[i][j] = ' '
                    maze[i][j + 1] = ' '
                    portal += 1

    # outer bottom part
    for i in range(rows - 1, rows - 2, -1):
        for j in range(0, cols):
            if maze[i][j] != ' ':
                if maze[i][j] == 'A' and maze[i - 1][j] == 'A':   
                    maze[i - 2][j] = 's'
                    maze[i][j] = ' '
                    maze[i - 1][j] = ' '
                elif maze[i][j] == 'Z' and maze[i - 1][j] == 'Z':
                    maze[i - 2][j] = 'e'
                    maze[i][j] = ' '
                    maze[i - 1][j] = ' '
                else:       # portal found
                    portals[maze[i][j] + maze[i - 1][j]] = portal # memorize
                    maze[i - 2][j] = str(portal)
                    maze[i][j] = ' '
                    maze[i - 1][j] = ' '
                    portal += 1


        # outer right part
    for i in range(0, rows):
        for j in range(cols - 1, cols - 2, -1):
            if maze[i][j] != ' ':
                if maze[i][j] == 'A' and maze[i][j - 1] == 'A':   
                    maze[i][j - 2] = 's'
                    maze[i][j] = ' '
                    maze[i][j - 1] = ' '
                elif maze[i][j] == 'Z' and maze[i][j - 1] == 'Z':
                    maze[i][j - 2] = 'e'
                    maze[i][j] = ' '
                    maze[i][j - 1] = ' '    
                else:       # portal found
                    portals[maze[i][j] + maze[i][j - 1]] = portal # memorize
                    maze[i][j - 2] = str(portal)
                    maze[i][j] = ' '
                    maze[i][j - 1] = ' '
                    portal += 1




    printMaze(maze, rows, cols)
    print(portals)

            


