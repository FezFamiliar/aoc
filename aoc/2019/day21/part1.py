def solveMaze(maze, i, j, rows, cols):

    global steps


    if maze[i + 1][j] == 'e':
        print(f"Total number of steps: {steps}")

        maze[i][j] = 'v'
        exit(1)
    if maze[i][j + 1] == 'e':
        print(f"Total number of steps: {steps}")

        maze[i][j] = '>'
        exit(1)
    if maze[i - 1][j] == 'e':
        print(f"Total number of steps: {steps}")

        maze[i][j] = '^'
        exit(1)
    if maze[i][j - 1] == 'e':
        print(f"Total number of steps: {steps}")
        maze[i][j] = '<'
        exit(1)
    
    if type(maze[i + 1][j]) == int:         # portal bottom
        

        p = maze[i + 1][j]
        # printMaze(maze, rows, cols)
