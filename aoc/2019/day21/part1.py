            if portal_pos[p][0] == i + 1 and portal_pos[p][1] == j:   # if its entrance 
                #warp to exit
                steps += 1
                solveMaze(maze, portal_pos[p][2], portal_pos[p][3], rows, cols)
                steps -= 1
