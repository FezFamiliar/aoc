def parseMaze(input):

    input = input.split('\n')

    cols = len(input[0])
    rows = len(input)
    
    maze = []
    for i in range(0, rows):
        _r = [char for char in input[i]]
        maze.append(_r)



    portal = 1
    # outer top part
    for i in range(0, 2):
        for j in range(0, cols):
            if maze[i][j] != ' ':
                if maze[i][j] == 'A' and maze[i + 1][j] == 'A':   
                    maze[i + 2][j] = 's'
                    maze[i][j] = ' '
                    maze[i + 1][j] = ' '
                    portal_pos['s'] = (i + 2, j)
                elif maze[i][j] == 'Z' and maze[i + 1][j] == 'Z':
                    maze[i + 2][j] = 'e'
                    maze[i][j] = ' '
                    maze[i + 1][j] = ' '
                    portal_pos['e'] = (i + 2, j)
                else:       # portal found
                    portals[maze[i][j] + maze[i + 1][j]] = portal # memorize
                    portal_pos[portal] = [0, 0, i + 2, j]     # ((entrace_i, entrace_j), (exit_i, exit_j))
                    maze[i + 2][j] = portal
                    maze[i][j] = ' '
                    maze[i + 1][j] = ' '
                    portal += 1


    # outer left part
    for i in range(0, rows):
        for j in range(0, 2):
            if maze[i][j] != ' ':
                if maze[i][j] == 'A' and maze[i][j + 1] == 'A':   
                    maze[i][j + 2] = 's'
                    maze[i][j] = ' '
                    maze[i][j + 1] = ' '
                    portal_pos['s'] = (i, j + 2)
                elif maze[i][j] == 'Z' and maze[i][j + 1] == 'Z':
                    maze[i][j + 2] = 'e'
                    maze[i][j] = ' '
                    maze[i][j + 1] = ' '
                    portal_pos['e'] = (i, j + 2)
                else:       # portal found
                    portals[maze[i][j] + maze[i][j + 1]] = portal # memorize
                    portal_pos[portal] = [0, 0, i, j + 2]
                    maze[i][j + 2] = portal
                    maze[i][j] = ' '
                    maze[i][j + 1] = ' '
                    portal += 1

    # outer bottom part
    for i in range(rows - 1, rows - 2, -1):
        for j in range(0, cols):
            if maze[i][j] != ' ':
                if maze[i][j] == 'A' and maze[i - 1][j] == 'A':   
                    maze[i - 2][j] = 's'
                    maze[i][j] = ' '
                    maze[i - 1][j] = ' '
                    portal_pos['s'] = (i - 2, j)
                elif maze[i][j] == 'Z' and maze[i - 1][j] == 'Z':
                    maze[i - 2][j] = 'e'
                    maze[i][j] = ' '
                    maze[i - 1][j] = ' '
                    portal_pos['e'] = (i - 2, j)
                else:       # portal found
                    portals[maze[i - 1][j] + maze[i][j]] = portal # memorize
                    portal_pos[portal] = [0, 0, i - 2, j]
                    maze[i - 2][j] = portal
                    maze[i][j] = ' '
                    maze[i - 1][j] = ' '
                    portal += 1



        # outer right part
    for i in range(0, rows):
        for j in range(cols - 1, cols - 2, -1):
            if maze[i][j] != ' ':
                if maze[i][j] == 'A' and maze[i][j - 1] == 'A':   
                    maze[i][j - 2] = 's'
                    maze[i][j] = ' '
                    maze[i][j - 1] = ' '
                    portal_pos['s'] = (i, j - 2)
                elif maze[i][j] == 'Z' and maze[i][j - 1] == 'Z':
                    maze[i][j - 2] = 'e'
                    maze[i][j] = ' '
                    maze[i][j - 1] = ' '    
                    portal_pos['e'] = (i, j - 2)
                else:       # portal found
                    portals[maze[i][j - 1] + maze[i][j]] = portal # memorize
                    portal_pos[portal] = [0, 0, i, j - 2]
                    maze[i][j - 2] = portal
                    maze[i][j] = ' '
                    maze[i][j - 1] = ' '
                    portal += 1

  # parse inner portals

    for i in range(2, rows - 2):
      for j in range(2, cols - 2):
          if str(maze[i][j]) in alphabet: # portal found

            if maze[i + 1][j] in alphabet: # inner top part or bottom part

              p = portals[maze[i][j] + maze[i + 1][j]]

              if maze[i - 1][j] == '.':   # inner top part
                maze[i - 1][j] = p
                portal_pos[p][0] = i - 1
                portal_pos[p][1] = j
                
              elif maze[i + 2][j] == '.': # inner bottom part
                maze[i + 2][j] = p    
                portal_pos[p][0] = i + 2 
                portal_pos[p][1] = j

              maze[i][j] = ' '
